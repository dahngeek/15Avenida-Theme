<h3>Introducción</h3>
<p>En vista de la próxima reunión de especialistas para el estudio de la música, se pidió al Patrimonio White hacer una compilación de los artículos escritos por Elena G. de White que tienen que ver con el asunto a ser considerado por la comisión. Al hacerlo se tomó en cuenta tanto el material publicado como el inédito. Tienen que ver, mayormente según lo entendemos, con los consejos que enuncian ciertos principios, para luego aplicar estos principios a fin de que los consejos de la pluma de Elena G. de White tengan una influencia directa en el asunto que ahora nos ocupa.</p>
<p>No intentamos hacer una presentación general en este documento, ni mucho menos una presentación exhaustiva. Es del todo probable que hayamos pasado por alto cierta declaración escogida que fue captada por algún miembro de la comisión.
</p>
<p>Creemos, sin embargo, que las declaraciones escogidas presentan los principios básicos, los cuales podrán ser efectivamente aplicados. Estos principios abarcan la función y las cualidades de la música, el impacto de la misma ya sea para bien o para mal, el uso efectivo de la música en los cultos de la iglesia y algunos consejos que pueden ser de interés para los compositores y directores de música.
</p>
<p>En la experiencia de Indiana en 1900 y 1901 a la cual se alude generalmente como el movimiento de la carne santificada (presentado en Mensajes Selectos, tomo 2, p. 35-45), la música aparece notablemente. Tenemos descripciones de testigos oculares de lo que sucedió, y también consejos de Elena de White basados en una visión que recibió en enero de 1900 en Australia, en el mismo principio del movimiento de la carne santificada, y en informes que le fueron enviados de parte del pastor Haskell y Sra., siete meses después de la visión.
</p>
<p>De esa combinación de materiales, encontramos ciertos principios que nos guiarán hoy.
</p>
<p>Así, sin querer hacer esta compilación demasiado detallada, pero tratando de incluir citas que suplen las necesidades del estudio de esta junta, presentamos este material.
</p>
<blockquote>
A.L. White, Secretario <br>
E.G. White Estate

<cite>Washington, D.C. , 30 de junio de 1992.</cite>

</blockquote>
